class Train {
  final String id;
  final String name;
  final String departure;
  final String arrival;
  final DateTime departureTime;
  final DateTime arrivalTime;

  Train({
    required this.id,
    required this.name,
    required this.departure,
    required this.arrival,
    required this.departureTime,
    required this.arrivalTime,
  });
}
